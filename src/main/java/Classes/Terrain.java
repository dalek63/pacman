    public int[] trouverPosition(char elementRecherche) {
        for (int i = 0; i < this.grilleActuel.length; i++) {
            for (int j = 0; j < this.grilleActuel[i].length; j++) {
                if (this.grilleActuel[i][j] == elementRecherche) {
                    return new int[]{i, j};
                }
            }
        }
        return null;
    }

